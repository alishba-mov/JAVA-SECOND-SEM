class RectangleArea {
    public static void main(String[] args) {

        int length = 10;
        int width = 5;

        int area = length * width; 

        // Displaying result
        System.out.println("Length: " + length);
        System.out.println("Width: " + width);
        System.out.println("Area of rectangle: " + area);
    }
}



